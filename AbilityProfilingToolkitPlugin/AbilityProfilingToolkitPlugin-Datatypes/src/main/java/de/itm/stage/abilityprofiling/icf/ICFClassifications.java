	public static String getICFType(String icf){
		if(icf.charAt(0)=='b'){
			return ICFTypes.BODY.toString();
		}
		if(icf.charAt(0)=='s'){
			return ICFTypes.STRUCTURE.toString();
		}
		if(icf.charAt(0)=='d'){
			return ICFTypes.DOMAIN.toString();
		}
		if(icf.charAt(0)=='e'){
			return ICFTypes.ENVIRONMENT.toString();
		}
		return null;
	}
	
